import { defineConfig, fontProviders } from 'astro/config';
import react from '@astrojs/react';
import sitemap from '@astrojs/sitemap';
import expressiveCode from 'astro-expressive-code';
import mdx from '@astrojs/mdx';
import pagefind from 'astro-pagefind';
import llms from 'astro-llms-md';
import icon from 'astro-icon';
import astroBrokenLinksChecker from 'astro-broken-links-checker';
import { nxViteTsPaths } from '@nx/vite/plugins/nx-tsconfig-paths.plugin';

export default defineConfig({
  site: 'https://atelier.pieper.io',
  fonts: [
    {
      name: 'Inter',
      cssVariable: '--font-sans',
      provider: fontProviders.google(),
      weights: [400, 500, 600, 700, 800],
      styles: ['normal'],
      display: 'swap',
    },
    {
      name: 'JetBrains Mono',
      cssVariable: '--font-mono',
      provider: fontProviders.google(),
      weights: [400, 500, 600],
      styles: ['normal'],
      display: 'swap',
    },
    {
      // Authorial signature accent (serif-italic keyword in headings).
      // Shared brand DNA with pieper.io — typography, not palette. See ADR-0020.
      name: 'Instrument Serif',
      cssVariable: '--font-accent',
      provider: fontProviders.google(),
      weights: [400],
      styles: ['italic'],
      display: 'swap',
    },
  ],
  integrations: [
    expressiveCode(),
    mdx(),
    react(),
    icon(),
    pagefind(),
    llms({
      generateIndividualMd: true,
      generateLlmsTxt: false,
      generateLlmsFullTxt: false,
    }),
    astroBrokenLinksChecker({
      checkExternalLinks: false,
      // Storybook subtrees are built after docs in the CF/Netlify build chain,
      // so /storybook-*/ links are flagged here as false positives. The
      // checker has no exclude option (upstream issue); throwError stays false.
    }),
    // Per-page OG images are generated by src/pages/og/[...slug].ts
    // (astro-og-canvas) from the OG_PAGES map; BaseLayout falls back to
    // /logo.png for pages without an entry.
    sitemap({
      changefreq: 'weekly',
      priority: 0.7,
      lastmod: new Date(),
      filter: (page) => !page.includes('/404'),
      serialize(item) {
        if (item.url === 'https://atelier.pieper.io/') {
          item.priority = 1.0;
          item.changefreq = 'weekly';
        } else if (/\/(workshop|storybook|figma|mcp|components)\/?$/.test(item.url)) {
          item.priority = 0.9;
        } else if (/\/components\/[^/]+\/?$/.test(item.url)) {
          item.priority = 0.8;
        }
        return item;
      },
    }),
  ],
  output: 'static',
  outDir: '../dist/docs',
  vite: {
    plugins: [nxViteTsPaths()],
    server: {
      port: 4300,
    },
  },
});
